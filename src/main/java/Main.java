import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import java.sql.Connection;
import java.sql.PreparedStatement;
import java.sql.ResultSet;
import java.sql.SQLException;

public class Main {
    private static final Logger logger = LoggerFactory.getLogger(Main.class);

    public static void main(String[] args) {
        try {
            // Получаем соединение с базой данных
            Connection connection = DatabaseManager.connect();




























































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































            // Пример выполнения запроса
            String query = "SELECT id, name FROM users";
            PreparedStatement statement = connection.prepareStatement(query);
            ResultSet resultSet = statement.executeQuery();

            // Обработка результатов запроса
            while (resultSet.next()) {
                // Получаем данные из результата запроса
                int id = resultSet.getInt("id");
                String name = resultSet.getString("name");

                // Делаем что-то с данными (например, выводим их)
                System.out.println("ID: " + id + ", Name: " + name);
            }

            // Закрываем ресурсы
            resultSet.close();
            statement.close();
            connection.close();
        } catch (SQLException e) {
            logger.error("Произошла ошибка при выполнении SQL запроса", e);
            // Обработка ошибок подключения к базе данных или выполнения запросов
        }
    }
}
